                    <p class="text-left">O IFSP lançou nesta terça-feira (17) o edital com as regras do Processo Seletivo com 5.962 vagas para cursos técnicos que serão ofertadas por 36 câmpus no primeiro semestre de 2020. As inscrições são gratuitas e devem ser realizadas de 23 de setembro a 10 de novembro. Para se inscrever, o candidato deve ler o edital na íntegra, criar um cadastro no portal https://processoseletivo.ifsp.edu.br e preencher o formulário de inscrição e o questionário socioeconômico. Cada candidato pode realizar uma única inscrição.</p>
                    <p class="text-left">São oferecidos cursos em três modalidades diferentes: 1) curso técnico integrado ao ensino médio: o candidato deve ter concluído o ensino fundamental. Ele cursará o ensino médio e o técnico no IFSP; 2) curso técnico concomitante ao ensino médio: o candidato deve estar matriculado no ensino médio em outra instituição de ensino. Ele cursará apenas o ensino técnico no IFSP; 3) curso técnico subsequente ao ensino médio: o candidato deve ter concluído o ensino médio. Ele cursará apenas o ensino técnico no IFSP.</p>
                    <p class="text-left">Assim como nas edições anteriores, a seleção dos candidatos será feita por meio de análise de histórico escolar. Não há provas! Os candidatos devem inserir as notas ou conceitos das disciplinas de Língua Portuguesa e Matemática. Para cursos Integrados, as notas a serem informadas são as do 8° ano (antiga 7ª série) do Ensino Fundamental. Já para os cursos concomitantes ou subsequentes, as notas a serem informadas são do 9° ano (antiga 8ª série) do Ensino Fundamental.</p>
                    <p class="text-left">No Instituto Federal de São Paulo você se prepara para ser um profissional tecnicamente capacitado e ao mesmo tempo um cidadão crítico e participativo. Aqui o estudante aprende que a tecnologia pode caminhar lado a lado com o desenvolvimento sustentável e que cuidar do planeta também é promover o progresso. Nossos alunos são capacitados para desenvolverem inovações tecnológicas voltadas para atender à necessidade de quem mais precisa e convivem em um ambiente que respeita a diversidade e promove igualdade social, racial e de gênero.</p>
